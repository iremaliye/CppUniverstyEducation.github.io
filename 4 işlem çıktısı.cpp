#include <iostream>
using namespace std;
int main()
{
	int sayi1,sayi2;
	cout <<"Lutfen birinci sayiyi giriniz:";
	cin >>sayi1;
	cout <<"Lutfen ikinci sayiyi giriniz:";
	cin >>sayi2;
	cout <<"Toplam:"<<sayi1+sayi2<<endl;
	cout <<"Cikarma:"<<sayi1-sayi2<<endl;
	cout <<"Carpma:"<<sayi1*sayi2<<endl;
	cout <<"Bolme:"<<(float)sayi1/sayi2<<endl;
	cout <<"Kalan:"<<sayi1%sayi2<<endl;
	
	return 0;
}
